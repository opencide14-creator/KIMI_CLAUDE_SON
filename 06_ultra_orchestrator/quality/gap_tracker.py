"""
BUILD GAP Code Tracker — SKILL_TRAINING_LAWS_v03 §5

Tracks all gap codes across the build process:
  H: Handcraft violations     Q: Quality threshold failures
  AB: A/B run problems        CA: Cognitive architecture gaps
  TI: Trigger incoherence     PL: Placeholder laundering
  SC: Score gaming            ML: Meta-learning failures
  ME: Meta-evaluation gaps

Every gap is tracked with: code, severity, component, fix_strategy
"""

from __future__ import annotations

import json, time, re, hashlib
from dataclasses import dataclass, field, asdict
from typing import Optional, List, Dict, Set, Tuple
from enum import Enum
import logging

logger = logging.getLogger("GAP")


class GAPCategory(str, Enum):
    HANDCRAFT = "H"      # H-01 script-generated, H-02 template not filled, H-03 parafrase
    QUALITY = "Q"        # Q-01 κ under threshold, Q-02 training data κ under 0.80
    AB_RUN = "AB"        # AB-01 fake run, AB-02 multiple variables, AB-03 no causal
    COGNITIVE = "CA"     # CA-01 generic CoT, CA-02 missing decision point
    TRIGGER = "TI"       # TI-01 SKILL.md ≠ TRAINING_PROMPT.md
    PLACEHOLDER = "PL"   # PL-01 placeholder string in final content
    SCORE = "SC"         # SC-01 generic Q/A, SC-02 κ-rubric gaming
    METALEARN = "ML"     # ML-01 missing prompt, ML-02 describes skill not learning
    METAEVAL = "ME"      # ME-01 eval without meta-eval, ME-02 no false negatives


